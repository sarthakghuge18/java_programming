package literals;

//literals =>
        // constant value is assigned to the variables is called as a literals 
        //the value is given at the time of declaration 


//keyword =>
        // words are reserved and used by the java compiler 
        //they cant be use as identifier
        
public class Literals {
    public static void main(String[] args) {
        char ch ='a';
        float f1 =4.5f;
        double d =4.5D;
        System.out.println(ch);
        System.out.println(f1);
        System.out.println(d);
    }
}
